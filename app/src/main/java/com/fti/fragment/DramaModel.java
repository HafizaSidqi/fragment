package com.fti.fragment;

public class DramaModel {
    private String judulDrama, sinopsisDrama;
    private int posterDrama;

    public String getJudulDrama() {
        return judulDrama;
    }

    public void setJudulDrama(String judulDrama) {
        this.judulDrama = judulDrama;
    }

    public int getPosterDrama() {
        return posterDrama;
    }

    public void setPosterDrama(int posterDrama) {
        this.posterDrama = posterDrama;
    }

    public String getSinopsisDrama() {
        return sinopsisDrama;
    }

    public void setSinopsisDrama(String sinopsisDrama) {
        this.sinopsisDrama = sinopsisDrama;
    }
}
